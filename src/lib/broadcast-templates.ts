/* ────────────────────────────────────────────────────────────────────────
 * 12ヶ月の編集カレンダー（堀A＝名簿に「毎月送る中身」を与える・H4）。
 *
 * 思想（[[fable5-master-plan-2026-06]] / [[revenue-model-weekly-2026-06]]）：
 *  名簿は「何を送るか」が決まって初めて資産になる。受験の年間サイクルに沿って
 *  毎月のテーマを先に設計し、配信時はここを参照するだけにする（運用の属人性を消す）。
 *
 * 名簿の二系統で分岐（H4要件）：
 *  - student（LINE名簿）＝生徒本人。CTAは無料ツール（計算・逆算・上げ方）＝行動の即時化。
 *  - parent（メール名簿）＝保護者＝決裁者。CTAは換金面（学費/塾代/FP/面談）＝高単価リード。
 *
 * 捏造ゼロ：県・年度で変わる確定日付は持たない。「〜ごろ」「お住まいの地域の日程を確認」で逃がす。
 * ──────────────────────────────────────────────────────────────────────── */

/** 配信対象。LINE名簿＝生徒／メール名簿＝保護者で出し分ける。 */
export type Audience = 'student' | 'parent';

/** 1配信ぶんの中身（newsletter.ts のレンダラがそのまま使える最小形）。 */
export interface AudienceMessage {
  subject: string;
  body: string;
  cta: { label: string; path: string };
}

/** ある月の配信設計（生徒・保護者の2系統を内包）。 */
export interface MonthlyBroadcast {
  /** 暦の月（1〜12）。 */
  month: number;
  /** その月のテーマ（運用カレンダーの見出し）。 */
  theme: string;
  /** 配信の狙い・ピーク月かどうかの運用メモ。 */
  intent: string;
  /** 名簿velocity/換金のピーク月か（8月・11月・12月・2月）。配信頻度の優先付けに使う。 */
  priority: 'peak' | 'normal';
  /** 生徒向け（LINE名簿）。 */
  student: AudienceMessage;
  /** 保護者向け（メール名簿）。 */
  parent: AudienceMessage;
}

